export const languageOptions = [
  { value: 'ja' },
  { value: 'vi' },
];

const STORAGE_KEY = 'jpTaxiLanguage';
export const LANGUAGE_EVENT = 'jpTaxiLanguageChanged';

export function getStoredProfileLanguage() {
  const saved = localStorage.getItem(STORAGE_KEY);
  return languageOptions.some((item) => item.value === saved) ? saved : 'ja';
}

export function setStoredProfileLanguage(language) {
  const nextLanguage = languageOptions.some((item) => item.value === language) ? language : 'ja';
  localStorage.setItem(STORAGE_KEY, nextLanguage);
  window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: { language: nextLanguage } }));
  return nextLanguage;
}

